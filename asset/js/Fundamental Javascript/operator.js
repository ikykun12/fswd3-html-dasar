// 2. Operator:
// Operator Aritmatika
let x = 10;
let y = 5;
let z = x + y; // Penjumlahan
let a = x - y; // Pengurangan
let b = x * y; // Perkalian
let c = x / y; // Pembagian
let d = x % y; // Modulus

// Operator Perbandingan
let i = 10;
let j = 5;
console.log(i > j); // true
console.log(i < j); // false
console.log(i >= j); // true
console.log(i <= j); // false
console.log(i == j); // false
console.log(i != j); // true

// Operator Logika
let k = true;
let l = false;
console.log(k && l); // false
console.log(k || l); // true
console.log(!k); // false
